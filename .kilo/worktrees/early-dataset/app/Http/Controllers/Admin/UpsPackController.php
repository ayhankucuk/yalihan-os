use App\Http\Controllers\Controller;
use App\Models\FeaturePack;
use App\Models\FeatureCategory;
use Illuminate\Http\Request;

/**
 * @deprecated 2026-04-05 Legacy UPS pack management. Routes active (admin.php L1583-1587).
 * ⚠️ QUARANTINE: Do not add new methods. Evaluate consolidation into PropertyHub.
 * Depends on: UpsFeaturePackService (still active).
 */
class UpsPackController extends Controller
{
    protected $packService;

    public function __construct(\App\Services\Ups\UpsFeaturePackService $packService)
    {
        $this->packService = $packService;
    }

    public function index()
    {
        // Fetch packs with their features count to display in the UI
        $packs = FeaturePack::withCount('features')->orderBy('display_order')->get(); // context7-ignore
        return view('admin.ups.packs.index', compact('packs'));
    }

    /**
     * Get features for a specific pack (assigned + all available)
     */
    public function getFeatures(FeaturePack $pack)
    {
        // Get all feature categories with their features
        $categories = FeatureCategory::with(['features' => function($q) {
            $q->orderBy('display_order', 'asc'); // context7-ignore
        }])->orderBy('display_order', 'asc')->get(); // context7-ignore

        // Get IDs of features currently assigned to the pack
        $assignedFeatureIds = $pack->features()->pluck('features.id')->toArray();

        return response()->json([
            'pack' => $pack,
            'categories' => $categories,
            'assigned_ids' => $assignedFeatureIds
        ]);
    }

    /**
     * Store a newly created pack in storage.
     */
    public function store(Request $request)
    {
        $validated = $request->validate([
            'name' => 'required|string|max:255',
            'description' => 'nullable|string',
            'aktiflik_durumu' => 'boolean',
        ]);

        // slug will be generated by model observer
        $pack = $this->packService->createPack([
            'name' => $validated['name'],
            'description' => $validated['description'] ?? null,
            'aktiflik_durumu' => $request->boolean('aktiflik_durumu', true),
            'display_order' => FeaturePack::max('display_order') + 1
        ]);

        if ($request->expectsJson()) {
            return response()->json([
                'success' => true,
                'message' => 'Yeni paket başarıyla oluşturuldu.',
                'pack' => $pack
            ]);
        }

        return redirect()->back()->with('success', 'Yeni paket başarıyla oluşturuldu.');
    }

    /**
     * Update features for a specific pack
     */
    public function updateFeatures(Request $request, FeaturePack $pack)
    {
        $request->validate([
            'feature_ids' => 'present|array',
            'feature_ids.*' => 'exists:features,id'
        ]);

        // Sync features to the pack
        $pack->features()->sync($request->feature_ids);

        // Trigger observer
        $pack->touch();

        return response()->json([
            'success' => true,
            'message' => 'Paket özellikleri güncellendi.',
            'count' => count($request->feature_ids)
        ]);
    }

    /**
     * Remove the specified pack from storage.
     */
    public function destroy(FeaturePack $pack)
    {
        $this->packService->deletePack($pack);

        if (request()->expectsJson()) {
            return response()->json([
                'success' => true,
                'message' => 'Paket başarıyla silindi.'
            ]);
        }

        return redirect()->back()->with('success', 'Paket başarıyla silindi.');
    }
}
