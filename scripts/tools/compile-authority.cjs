#!/usr/bin/env node

/**
 * Authority Compiler
 *
 * Source: .sab/authority.json
 * Target: config/context7_guard.php
 *
 * Responsibilities:
 * 1. Read Authority JSON SSOT
 * 2. Extract databaseGuards
 * 3. Generate PHP Config File
 */

const fs = require('fs');
const path = require('path');
const crypto = require('crypto');

const AUTHORITY_PATH = path.join(process.cwd(), '.sab', 'authority.json');
const CONFIG_PATH = path.join(process.cwd(), 'config', 'context7_guard.php');
const MANIFEST_DIR = path.join(process.cwd(), 'storage', 'app', 'context7', 'generated');
const MANIFEST_PATH = path.join(MANIFEST_DIR, 'guards.manifest.json');

function generateGuardConfig(authority) {
    // SAB migration: support both legacy databaseGuards and new governance.table_specific_guards
    const rawGuards = authority.databaseGuards || authority.governance?.table_specific_guards || {};

    // 🛡️ Deterministic Sorting: Sort table names alphabetically
    const guards = Object.keys(rawGuards).sort().reduce((acc, table) => {
        acc[table] = rawGuards[table];
        return acc;
    }, {});

    let php = `<?php

/**
 * 🛡️ SAB DATABASE GUARD CONFIGURATION
 *
 * @generated_from .sab/authority.json
 *
 * 🛑 DO NOT EDIT THIS FILE MANUALLY.
 * 💡 Run 'npm run authority:compile' to regenerate.
 */

return [
    'generated_from' => '.sab/authority.json',
    'last_updated' => '${authority.last_updated || "STABLE"}',
    'tables' => [
`;

    for (const [table, rules] of Object.entries(guards)) {
        php += `        '${table}' => [\n`;

        // Forbidden Columns — support both legacy (forbidden_columns) and SAB (forbidden)
        const forbiddenCols = (rules.forbidden_columns || rules.forbidden || []).sort();
        if (forbiddenCols.length > 0) {
            php += `            'forbidden' => [\n`;
            forbiddenCols.forEach((col) => {
                php += `                '${col}',\n`;
            });
            php += `            ],\n`;
        }

        // Canonical Map
        if (rules.canonical) {
            if (typeof rules.canonical === 'string') {
                php += `            'canonical' => '${rules.canonical}',\n`;
            } else {
                php += `            'canonical' => [\n`;
                // Sort canonical keys for determinism
                const sortedCanonicalKeys = Object.keys(rules.canonical).sort();
                for (const legacy of sortedCanonicalKeys) {
                    const canonical = rules.canonical[legacy];
                    php += `                '${legacy}' => '${canonical}',\n`;
                }
                php += `            ],\n`;
            }
        }

        // Required Columns
        const requiredCols = (rules.required_columns || []).sort();
        if (requiredCols.length > 0) {
            php += `            'required' => [\n`;
            requiredCols.forEach((col) => {
                php += `                '${col}',\n`;
            });
            php += `            ],\n`;
        }

        // Message
        if (rules.message) {
            php += `            'message' => "${rules.message.replace(/"/g, '\\"')}",\n`;
        }

        php += `        ],\n`;
    }

    php += `    ],
];
`;
    return php;
}

function compile(write = true) {
    if (write) console.log('🔄 Compiling Authority Guard...');

    if (!fs.existsSync(AUTHORITY_PATH)) {
        console.error('❌ Missing Authority SSOT:', AUTHORITY_PATH);
        if (write) process.exit(1);
        throw new Error('Missing Authority SSOT');
    }

    const authorityContent = fs.readFileSync(AUTHORITY_PATH, 'utf8');
    const authority = JSON.parse(authorityContent);
    const sourceHash = crypto.createHash('sha256').update(authorityContent).digest('hex');

    const phpContent = generateGuardConfig(authority);

    // Manifest (Deterministic: no generated_at timestamp)
    const manifest = {
        source: '.sab/authority.json',
        source_hash: sourceHash,
        version: authority.version || 'unknown',
        files: ['config/context7_guard.php'],
    };

    if (write) {
        // Ensure Manifest Dir
        if (!fs.existsSync(MANIFEST_DIR)) {
            fs.mkdirSync(MANIFEST_DIR, { recursive: true });
        }

        fs.writeFileSync(CONFIG_PATH, phpContent);
        fs.writeFileSync(MANIFEST_PATH, JSON.stringify(manifest, null, 2));

        console.log('✅ Compiled to:', CONFIG_PATH);
        console.log('✅ Manifest:', MANIFEST_PATH);
    }

    return {
        phpContent,
        manifest,
    };
}

// Execute if run directly
if (require.main === module) {
    compile();
}

module.exports = { compile, generateGuardConfig, AUTHORITY_PATH, CONFIG_PATH };
